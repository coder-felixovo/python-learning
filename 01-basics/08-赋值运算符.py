# 赋值运算符

x = 10

x += 1
print(x)  # 11

x -= 1
print(x)  # 10

x *= 10
print(x)  # 100

x /= 10
print(x)  # 10.0

x = 10
x %= 3
print(x)  # 1

x = 10
x //= 3
print(x)  # 3

x = 10
x **= 2
print(x)  # 100

x = 10
x &= 3
print(x)  # 2

x = 10
x |= 3
print(x)  # 11

x = 10
x ^= 3
print(x)  # 9

x = 10
x >>= 3
print(x)  # 1

x = 10
x <<= 3
print(x)  # 80

